    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tableDashboard.getSelectedRow();
        if (row == -1) { // No row selected
            JOptionPane.showMessageDialog(this,
                    "No vaccine selected. Please select one to delete.",
                    "Error",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            DefaultTableModel model = (DefaultTableModel) tableDashboard.getModel();
            model.removeRow(row);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        DefaultTableModel model = (DefaultTableModel) tableDashboard.getModel();
        Vector<Vector> tableData = model.getDataVector();
        try (
                FileOutputStream file = new FileOutputStream("file.bin"); ObjectOutputStream output = new ObjectOutputStream(file)) {
            output.writeObject(tableData);
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_formWindowClosing

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
         try (
                FileInputStream file = new FileInputStream("file.bin"); 
                ObjectInputStream input = new ObjectInputStream(file)) {

                // Load vaccine data
                Vector<Vector> tableData = (Vector<Vector>) input.readObject();
                DefaultTableModel model = (DefaultTableModel) tableDashboard.getModel();

                // Clear existing rows
                model.setRowCount(0);

                // Add rows for vaccine data
                for (Vector row : tableData) {
                    model.addRow(row.toArray());
                }

               ObjectInputStream input2 = new ObjectInputStream(new FileInputStream("book.bin"));
                Vector<Booking> bookingList = (Vector<Booking>) input2.readObject();
                DefaultTableModel model2 = (DefaultTableModel) tableBookingList.getModel();
                model2.setRowCount(0);
                for (Booking booking : bookingList) {
                    Vector<Object> row = new Vector<>();
                    row.add(booking.getVaccineId());
                    row.add(booking.getVaccineName());
                    row.add(booking.getName());
                    row.add(booking.getEmail());
                    model2.addRow(row);
                }

            } catch (Exception e) {
                e.printStackTrace();
        }
    }//GEN-LAST:event_formWindowOpened

    private void btnExitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExitActionPerformed
        // TODO add your handling code here:
        int confirm = JOptionPane.showConfirmDialog(this,
                "Are you sure you want to exit?",
                "Exit Confirmation",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            DefaultTableModel model = (DefaultTableModel) tableDashboard.getModel();
            Vector<Vector> tableData = model.getDataVector();
            try (
                    FileOutputStream file = new FileOutputStream("file.bin"); ObjectOutputStream output = new ObjectOutputStream(file)) {
                output.writeObject(tableData);
            } catch (Exception e) {
                e.printStackTrace();
            }
            System.exit(0); // Closes the program
            
        }

    }//GEN-LAST:event_btnExitActionPerformed

