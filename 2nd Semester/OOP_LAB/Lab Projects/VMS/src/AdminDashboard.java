    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String id = tfID.getText();
        String name = tfName.getText();
        String menu = tfMenu.getText();
        String doses = tfDoses.getText();
        if (id.isEmpty() || name.isEmpty() || menu.isEmpty() || doses.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Please enter all fields",
                    "Input Error",
                    JOptionPane.ERROR_MESSAGE);
        } else if (!doses.matches("\\d+")) { // Check if Doses is numeric
            JOptionPane.showMessageDialog(this,
                    "Doses must be a numeric value.",
                    "Input Error",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            DefaultTableModel model = (DefaultTableModel) tableDashboard.getModel();
            model.addRow(new Object[]{id, name, menu, doses});
            // Clear the form
            tfID.setText("");
            tfName.setText("");
            tfMenu.setText("");
            tfDoses.setText("");
    }//GEN-LAST:event_btnAddActionPerformed
    }
